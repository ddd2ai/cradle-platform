import assert from "assert";
import { createProjectCommands } from "../src/commands/project-commands.js";

function captureConsoleAsync(fn) {
  const originalLog = console.log;
  const output = [];

  console.log = (...args) => output.push(args.join(" "));

  return Promise.resolve()
    .then(fn)
    .then(() => output.join("\n"))
    .finally(() => {
      console.log = originalLog;
    });
}

function createEngine({ cradleMode = false } = {}) {
  const writes = [];
  const cell = {
    id: "cell-001",
    writeWorkspaceFile: async (fileName, content) => writes.push({ fileName, content }),
  };

  return {
    writes,
    isCradleMode: () => cradleMode,
    getActiveCell: () => cell,
  };
}

const commands = createProjectCommands();
assert.deepEqual(
  commands.map((command) => command.name),
  ["/project-init", "/project-file"]
);

const byName = new Map(commands.map((command) => [command.name, command]));
assert.equal(byName.get("/project-init").match("/project-init alpha", { engine: createEngine() }), true);
assert.equal(
  byName.get("/project-init").match("/project-init alpha", { engine: createEngine({ cradleMode: true }) }),
  false
);

const initEngine = createEngine();
const initOutput = await captureConsoleAsync(() =>
  byName.get("/project-init").execute({
    engine: initEngine,
    input: "/project-init alpha",
  })
);
assert.equal(initEngine.writes[0].fileName, "projects/alpha/README.md");
assert.ok(initEngine.writes[0].content.includes("# alpha"));
assert.ok(initOutput.includes("Project initialized: projects/alpha"));

const fileEngine = createEngine();
const fileOutput = await captureConsoleAsync(() =>
  byName.get("/project-file").execute({
    engine: fileEngine,
    input: "/project-file alpha docs/plan.md",
  })
);
assert.equal(fileEngine.writes[0].fileName, "projects/alpha/docs/plan.md");
assert.ok(fileEngine.writes[0].content.includes("generated by cell-001"));
assert.ok(fileOutput.includes("Project file created: projects/alpha/docs/plan.md"));

const usageOutput = await captureConsoleAsync(() =>
  byName.get("/project-file").execute({
    engine: createEngine(),
    input: "/project-file alpha",
  })
);
assert.equal(usageOutput, "Usage: /project-file <project-name> <relative-file-path>");

console.log("Project command tests passed");
