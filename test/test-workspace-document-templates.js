import assert from "assert";
import {
  createDecisionDocument,
  createNoteDocument,
  createProjectFileDocument,
  createProjectReadmeDocument,
  createResearchDocument,
} from "../src/commands/workspace-document-templates.js";

const createdAt = "2026-07-24T10:11:12.000Z";

const note = createNoteDocument({
  content: "Remember the evidence",
  createdAt,
});
assert.ok(note.startsWith("# Note"));
assert.ok(note.includes("Remember the evidence"));
assert.ok(note.includes(`createdAt: ${createdAt}`));

const decision = createDecisionDocument({
  content: "Use small refactor slices",
  createdAt,
});
assert.ok(decision.startsWith("# Decision"));
assert.ok(decision.includes("Use small refactor slices"));
assert.ok(decision.includes("TODO: describe why this decision was made."));
assert.ok(decision.includes(`createdAt: ${createdAt}`));

const research = createResearchDocument({
  content: "Compare lifecycle boundaries",
  createdAt,
});
assert.ok(research.startsWith("# Research"));
assert.ok(research.includes("Compare lifecycle boundaries"));
assert.ok(research.includes("manual"));
assert.ok(research.includes("TODO: summarize key findings."));

const readme = createProjectReadmeDocument({
  projectName: "cradle-runtime",
  createdAt,
});
assert.ok(readme.startsWith("# cradle-runtime"));
assert.ok(readme.includes("TODO: describe project purpose."));
assert.ok(readme.includes("TODO: describe project structure."));

const projectFile = createProjectFileDocument({
  cellId: "cell-001",
  createdAt,
});
assert.ok(projectFile.includes("// TODO: generated by cell-001"));
assert.ok(projectFile.includes(`// createdAt: ${createdAt}`));

console.log("Workspace document template tests passed");
